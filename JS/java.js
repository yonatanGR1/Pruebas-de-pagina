function Menu(){
    document.getElementById("contenedor-menu").style.visibility = "visible";
    document.getElementById("contenedor-menu").style.opacity = "1";
}

function cerrar(){
    document.getElementById("contenedor-menu").style.visibility = "hidden";
    document.getElementById("contenedor-menu").style.opacity = "0";
}

function ITE(){
    document.getElementById("ITE").style.visibility = "visible";
    document.getElementById("Ocultar").style.visibility = "hidden";
}
